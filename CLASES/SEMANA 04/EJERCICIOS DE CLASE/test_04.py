"""Listas en Python"""

"""
Listas, 
remove(): Elimina un elemento de la lista, indicando el valor
"""

var_1 = ["Python", "Javascript", "JAVA", "PHP", "Typescript"]

print("Mi lista tiene los siguiente valores: {}".format(var_1))

var_1.remove("PHP")
var_1.remove("JAVA")

print("Mi lista actualizada es: {}".format(var_1))

# Comprobando metodo
print(var_1)

